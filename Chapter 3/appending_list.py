#Appending an item to list will add the new element to the end of the list
motorcycles = ['honda','yamaha','suzuki']
print(motorcycles)

#Using the append method to add a new element at the end of the list
motorcycles.append('ducati')
print(motorcycles)

#Appending to an empty list
motorcycles = []

motorcycles.append('honda')
motorcycles.append('yamaha')
motorcycles.append('suzuki')

print(motorcycles)